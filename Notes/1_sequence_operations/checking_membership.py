# Checking Membership - Test whether an item is or is not in a sequence.

#string
x = 'llama'
print('m' in x)
#output: True
#m is found in llama, so the statement is true

#list
y = ['happy', 'llama', 'explorer']
print('happy' not in y)
#output: false 
#happy is found in y, so the statement is false

#tuple
z = ('Itadori', 'Fushigiro', 'Nobara', 'Gojo')
print('Fushigiro' in z)
#output: true
#Fushigiro is found in z, so the statement is true