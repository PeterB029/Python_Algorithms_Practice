#Sorting - returns a NEW list of items in sorted order.
#Does NOT change the original list!

#string
x = 'bug'
print(sorted(x))
#output: ['b', 'g', 'u']

#list
y = ['pig', 'cow', 'horse']
print(sorted(y))
#output: ['cow', 'horse', 'pig']

#tuple
z = ('Itadori', 'Fushigiro', 'Nobara', 'Gojo')
print(sorted(z))
#output: ['Fushigiro', 'Gojo', 'Itadori', 'Nobara']